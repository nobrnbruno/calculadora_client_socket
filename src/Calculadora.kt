class Calculadora {
    fun soma(oper1: Double, oper2: Double): Double {
        return oper1 + oper2
    }

    fun subtracao(oper1: Double, oper2: Double): Double {
        return oper1 - oper2
    }

    fun multiplicacao(oper1: Double, oper2: Double): Double {
        return oper1 * oper2
    }

    fun divisao(oper1: Double, oper2: Double): Double {
        return oper1 / oper2
    }
}